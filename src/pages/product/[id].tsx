export const getServerSideProps: GetServerSideProps = async ({ params }) => {
  const product = await prisma.product.findUnique({
    where: {
      id: params?.id as string,
    },
  });

  return {
    props: {
      product: JSON.parse(JSON.stringify(product)),
    },
  };
};
